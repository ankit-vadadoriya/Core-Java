public class AV_21_while_loop {
    public static void main(String[] args) {
        int i = 1;
        while (i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("End while loop");

        System.out.println("Print natural number 100 to 200");
        int n = 100;
        while (n<=200){
            System.out.print(n + ", ");
            n++;
        }
    }
}
