package Java_DSA;

public class A_03_if_else {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        if (a < b) {
            System.out.println("smaller");
        } else if(a > b) {
            System.out.println("greater");
        } else {
            System.out.println("equal");
        }
    }
}
